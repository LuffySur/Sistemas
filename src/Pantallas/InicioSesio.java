    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        // TODO add your handling code here:
        if(txtEmail.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"Se requiere E-mail");
            txtEmail.grabFocus();
            
        }
        else{
        
        if(txtPassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"Se requiere password");
            txtPassword.grabFocus();  
            }
        else {
        
            try{
               String consulta="SELECT correo,contrasena FROM usuario "+ 
                    "WHERE correo LIKE ? AND contrasena LIKE ?";   
                cmd=(PreparedStatement)conexion.conectar.prepareStatement(consulta);            
                cmd.setString(1,txtEmail.getText());
                Cifrar x=new Cifrar();
                String contra=x.getPassword(txtPassword.getText().toUpperCase());
                //JOptionPane.showMessageDialog(rootPane, contra);
                cmd.setString(2,contra);
                tupla= cmd.executeQuery();
                 if(tupla.next()){
                   
                    if(tupla.getString(2).matches(contra)){
                        JOptionPane.showMessageDialog(rootPane,"Bienvenid@!");
                        Principal p=new Principal();

                        
                        p.setVisible(true);
                    }
                    else{
                 
                        JOptionPane.showMessageDialog(rootPane,"Password incorrecto"); 
                    }
                }
            
                else{
                    JOptionPane.showMessageDialog(rootPane,"Usuario inexistente");
                }
            
            } catch(SQLException e){
              JOptionPane.showMessageDialog(rootPane, "Error en consulta"+e);
            }  
        }        
    }
       
    }//GEN-LAST:event_btnloginActionPerformed

