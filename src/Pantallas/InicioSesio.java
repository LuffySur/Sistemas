    private void txtPasswordKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyTyped
        // TODO add your handling code here:
        
        StringBuilder cadena=new StringBuilder(txtPassword.getText());
        if(valida==false){
          if(evt.isShiftDown()==true){
            if(evt.getKeyChar()<'A'||evt.getKeyChar()>'Z'){
               if(evt.getKeyChar()<'a'||evt.getKeyChar()>'z'){
                  if(evt.getKeyChar()<'0'||evt.getKeyChar()>'9'){
                     if(evt.getKeyChar()!='*'&&evt.getKeyChar()!='_'&&
                        evt.getKeyChar()!='#'&&evt.getKeyChar()!='&'){
                  
                         JOptionPane.showMessageDialog(rootPane," Caracter invalido ");
                         cadena.deleteCharAt(cadena.length()-1);
                         txtPassword.setText(cadena.toString());
                         cadena=null;
                         
                  }
                  }
               } 
            }
          }
          else{
          
              if(evt.getKeyChar()<'A'||evt.getKeyChar()>'Z'){
               if(evt.getKeyChar()<'a'||evt.getKeyChar()>'z'){
                  if(evt.getKeyChar()<'0'||evt.getKeyChar()>'9'){
                     if(evt.getKeyChar()!='*'&&evt.getKeyChar()!='_'&&
                        evt.getKeyChar()!='#'&&evt.getKeyChar()!='&'){
                  
                         JOptionPane.showMessageDialog(rootPane," Caracter invalido ");
                         cadena.deleteCharAt(cadena.length()-1);
                         txtPassword.setText(cadena.toString());
                        
                         
                  }
                  }
               } 
            }
          
          }
        }
        valida=false;
    }//GEN-LAST:event_txtPasswordKeyTyped

